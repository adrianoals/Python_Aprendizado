#Operador Not
"""
O operador "not" vai inverter o valor de uum booleano;
Ou seja, quando criarmos um comparação que resulta em
booleano, podemos inverter com not;
"True" se torna "False" e "False" se torna "True"
 A  Not A
 1    0
 0    1
"""
verdadeiro = True
falso = False

print (not verdadeiro)
print (not falso)

print (not (5 > 2))
print (not (5 < 2))