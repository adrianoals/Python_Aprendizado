#Strings dinâmicas com float
"""
Para inserir os decimais temos uma sintaxe um pouco 
diferente dos demais tipos de dados;
Por causa do número de casas decimais que vamos 
exibir;
Exemplo:
print("Você deve R$%.2f ao banco." %valor)
""" 

pi = 3.1439247237842873478

print ("O valor de pi é %f" % pi)

print ("O valor de pi é %.10f" % pi)

print ("O valor de pi é %.2f" % pi)

motor = 2.0

print ("O motor do meu carro é %f" % motor)

print ("O motor do meu carro é %.1f" % motor)