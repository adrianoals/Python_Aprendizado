#Estruturas de Repetição
"""
Uma forma deexecutar a ação x vezes;
Evita a repetição de código desnecessariamente
A repetição será executada até satisfazer uma condição
no código;
Está condição é semelhante a que utilizamos no if,
realizamos uma comparação;
"""
#While
"""
While é uma das estruturas de repetição do Python;
A sintaxe do while é simples e dividida em três partes;
1- A variável para servir de contador;
2- Condição de execução do loop;
3- Incremento de variável;
Ex:
variavel = 0
while variavel < 5:
    variavel = variavel + 1
"""
#Instrução Break
"""
Com a instrução break podemos interoomper uma repetição;
Ela pode ser inserida após determinada condição no 
código;
O break fará que o loop seja cancelado totalmente, e o 
programa continue na próxima linha de código;
"""
#Repetições Aninhadas
"""
Podemos inserir um loop dentro do outro;
A ordem de execução é de fora para dentro;
O loop interno será executado toda iteração do 
loop externo;
Temos que ter cuidado com os iteradores, precisamos 
de um para cada loop;
"""
