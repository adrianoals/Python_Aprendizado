# Função Lambda
"""
A função lambda vem da programação funional;
Podemos criar uma função que executa uma simples expressão;
E atribuir o valor obtido a uma variável, veja:

x = lambda a: a + 10

print(x(10))
"""

soma = lambda x: x + 10
print(soma(50))

soma_dois_nun = lambda a, b: a + b
print(soma_dois_nun(5,8))

