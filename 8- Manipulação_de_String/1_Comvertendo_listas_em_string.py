#Convertendo string em lista
"""
As strings em Python podem ser facilmente convertidas em listas;
A função list transforma uma string em lista;
Podemos também transformar uma lista em string;
A função join transforma uma lista em string;
"""
palavra ="Testando"

print(list(palavra))
print(palavra[0])

lista = ["O", "rato", "roeu", "a", "roupa"]
print(" ".join(lista))

print(lista)


