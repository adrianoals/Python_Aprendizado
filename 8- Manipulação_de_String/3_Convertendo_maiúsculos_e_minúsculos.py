# Conversão de string maiúsculas e minúsculas
"""
Podemos converter uma string para maiúsculas e também 
minúsculas para Python;
A função lower converte todas as letras para minúsculas;
E a função upper converte todas as letras para maiúsculas;
"""
palavra = "MatheUS"
print(palavra.lower())
teste = "UMA FRASE EM CAPS"
print(teste.lower())

min = "tudo em MAIÚsculo"
print(min.upper())

min2 = (min.upper())
print(min2)
