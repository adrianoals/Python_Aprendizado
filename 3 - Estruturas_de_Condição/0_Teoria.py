# O que é lógica de programação
"""
Uma elaboração de sequencias para atingir determinado
objetivo;
Ou seja, linhas de código sendo executadas do nosso 
programa alterando os dados;
Porém são adicionados outros recursos como: estruturas
de condição e estruturas de repetição
"""
#O que são algoritmos
"""
Algoritmo é um passo a passo para resolver uma determinada
tarefa;
Ou seja, um software ou programa tem uma série de 
algoritmos;
Exemplo de algoritmo para trocar lâmpada:

Comprar lâmpadanova, colocar a escada próxima a lâmpada, 
retirar a lâmpada antiga, iserir a lâmpada nova, descartar 
a lâmpada antiga. 
"""
#Estruturas de condição
"""
Estruturas de condição servem para criar bifurcação 
no nosso código;
Ou seja, executar um determinado trecho apenas se uma
condição for verdadeira;
A mais famosa é a estrutura " if " porém temos o " else " 
e " elif "
"""
#Estrutura IF
"""
O " if " vai receber uma condição, que se for avaliada 
como verdadeira o software entra em um bloco de código
diferente;
Caso a condição não seja verdadeira, o bloco é ignorado;
A estrutura e a sintaxe é a seguinte:

Ex:
If condição:
    bloco a ser executado
"""
#Estrutura Else
"""
O else vai apenas ocorrer quando o if não for executado;
Ou seja, podemos executar outra parte do código para uma 
situação inversa do if;

Veja a estrutura:
if condição:
    código
else:
    código
"""
#If Aninhado
"""
Um if pode estar dentro de utro if, há essa possibilidade em
Python;
Chamamos o recurso de estruturas aninhadas;
Esse tipo de lógica nos permite criar regras ainda mais 
complexas e personalizadas para a nossa aplicação;
"""
#Estrutura Elif
"""
A estrutura elif é a forma  que temos de escrever 
outros ifs antes do else;
Ou seja, podemos fazer mais checagens no código 
antes de retornar um else;
Obs: o else não é obrigatório, podemos ter só if e elif;
"""
